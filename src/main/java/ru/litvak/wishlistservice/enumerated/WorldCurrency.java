    WorldCurrency(String description, String symbol) {
        this.description = description;
        this.symbol = symbol;
    }

    public static WorldCurrency fromCode(String code) {
        for (WorldCurrency currency : values()) {
            if (currency.name().equalsIgnoreCase(code)) {
                return currency;
            }
        }
        throw new IllegalArgumentException("Неизвестный код валюты: " + code);
    }

    public static WorldCurrency fromJavaCurrency(Currency javaCurrency) {
        return fromCode(javaCurrency.getCurrencyCode());
    }

    public static Set<Currency> getAllJavaCurrencies() {
        return Currency.getAvailableCurrencies();
    }
